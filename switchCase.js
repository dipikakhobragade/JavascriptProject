function weekDay(dayNum) {
    switch(dayNum){
        case 1:
            console.log('day 1');
            break;
        case 2:
            console.log(`day 2`);
            break;
        case 3:
            console.log(`day3`);
            break;
        default:
            console.log('invalid input');
            break;

    
}
}
weekDay(1);
weekDay(2);
weekDay(7);
